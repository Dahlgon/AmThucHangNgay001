package com.example.admin.amthuchangngay001.model;

public class About {
    private final String name;
    private final String des;

    public About(String name, String des) {
        this.name = name;
        this.des = des;
    }

    public String getName() {
        return name;
    }


    public String getDes() {
        return des;
    }

}
